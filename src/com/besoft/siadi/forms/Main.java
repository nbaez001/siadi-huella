    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        usuarioService = Injector.obtenerServicio(UsuarioService.class);
        Agencia a = (Agencia) jcbAgencia.getItemAt(jcbAgencia.getSelectedIndex());
        Usuario u = usuarioService.autenticar(new Usuario(0, new Colaborador(a), jtfUsuario.getText(), jtfContrasena.getText(), true));
        if (u != null) {
            SessionUtil.usuario=u;
            RegColaboradorForm rcf = new RegColaboradorForm();
            rcf.setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Error al autenticar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

